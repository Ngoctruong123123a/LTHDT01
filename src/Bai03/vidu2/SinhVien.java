package Bai03.vidu2;

public class SinhVien {
	public String hoten;
	public int namsinh;
	public String sdt;
	
	
	public String getHoten() {
		return hoten;
	}
	public void setHoten(String hoten) {
		this.hoten = hoten;
	}
	public int getNamsinh() {
		return namsinh;
	}
	public void setNamsinh(int yob) {
		this.namsinh = yob;
	}
	public String getSdt() {
		return sdt;
	}
	public void setSdt(String phone) {
		this.sdt = phone;
	}
	
}	
